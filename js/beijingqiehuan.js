var thebody = document.getElementsByTagName('body');

function changeSpring() {

    thebody[0].id = 'spring';
}

function changeSummer() {

    thebody[0].id = 'summer';
}

function changeAutumn() {

    thebody[0].id = 'autumn';
}

function changeWinter() {

    thebody[0].id = 'winter';
}
changeSummer();